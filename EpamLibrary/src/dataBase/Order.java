package dataBase;

public class Order {
private int id;
private int bookId;
private int userId;
public int getId() {
	return id;
}
public int getBookId() {
	return bookId;
}
public int getUserId() {
	return userId;
}
public void setId(int OrderId) {
	id = OrderId;
}
public void setBookId(int OrderBookId) {
	bookId = OrderBookId;
}
public void setUserId(int OrderUserId) {
	userId = OrderUserId;
}
	
}
